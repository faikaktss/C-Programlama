#include <stdio.h>
#include <stdlib.h>

int main()
{
    printf("My name is Faik\b"); // \b = ondan önceki bir karakteri siler
    printf("My name is faik\n"); // \n = bi satır aşağı iner
    printf("My name is faik\t"); // \t = tab kadar boşluk bırakır
    printf("My name is faik\'"); // \' = çift tırnak işaretini basar
    printf("My name is faik\\"); //  \\ = ters bölü işareti ekrana basar
    
    return 0 ;
}